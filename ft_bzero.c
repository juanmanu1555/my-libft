#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	count;
	char	*t;

	t = (char *)s;
	count = 0;
	if (n > 0)
	{
		while (count < n)
		{
			t[count] = 0;
			count++;
		}
	}
}
